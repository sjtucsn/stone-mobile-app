<script>
	export default {
		onLaunch: function() {
			uni.getStorage({
				key: 'user',
				success: data => {
					this.$store.dispatch('login', data.data).catch((e) => {
						uni.removeStorage({
							key: 'user',
							success() {
								uni.reLaunch({
									url: 'pages/login/login'
								})
							}
						})
					})
				},
				fail: () => {
					uni.reLaunch({
						url: 'pages/login/login'
					})
				}
			})	
			console.log('App Launch')
		},
		onShow: function() {
			console.log('App Show')
		},
		onHide: function() {
			console.log('App Hide')
		}
	}
</script>

